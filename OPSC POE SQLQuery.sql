CREATE TABLE APP_USER(
USER_NO CHAR(10),
USER_EMAIL VARCHAR(50) NOT NULL,
USER_PASSWORD VARCHAR(50) NOT NULL,
CONSTRAINT APP_USER PRIMARY KEY (USER_NO)
);

CREATE TABLE BOOK_DETAIL
(
ISBN VARCHAR (13),
BOOK_NAME VARCHAR (255),
BOOK_AUTHOR VARCHAR (255),
BOOK_DESCRIPTION VARCHAR (255),
CATEGORY VARCHAR (30),
CONSTRAINT PK_BOOK_DETAIL PRIMARY KEY (ISBN)
);

CREATE TABLE USER_BOOK
(
USER_ISBN CHAR(10),
ISBN VARCHAR (13),
USER_NO CHAR (10),
CONSTRAINT PK_USER_BOOK PRIMARY KEY (USER_ISBN),
CONSTRAINT FK_ISBN FOREIGN KEY(ISBN) REFERENCES BOOK_DETAIL(ISBN),
CONSTRAINT FK_USER FOREIGN KEY(USER_NO) REFERENCES APP_USER(USER_NO)

